<template>
  <div @mouseover="showOptions = true" @mouseleave="showOptions=false">
    <form class="small" v-show="showOptions">
      <label for="selectBox">Font size: </label>
      <select class="custom-select" id="selectBox" v-model="setFontSize">
        <option value="42">42px</option>
        <option value="48">48px</option>
        <option value="56">56px</option>
        <option value="64">64px</option>
      </select>
    </form>
    <p :style="styleObject">{{displayText}}</p>
  </div>
</template>

<script>
export default {
  data() {
    return {
      showOptions: false,
      setFontSize: ""
    };
  },
  props: {
    displayText: [String],
    containerHeight: {
      type: Number,
      default: 200
    }
  },
  computed: {
    styleObject() {
      return {
        height: this.containerHeight + "px",
        fontSize: this.setFontSize + "px"
      };
    }
  }
};
</script>

<style scoped>
p {
  font-family: "Tangerine", cursive;
  font-size: 42px;
  line-height: 42px;
  text-shadow: 2px 2px 2px #aaa;
  color: #4d4d4d;
  margin: 5px 0;
  border: 1px dotted gray;
  white-space: pre-line;
  overflow: hidden;
}
</style>
